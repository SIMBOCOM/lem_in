int		ft_strequ(char const *s1, char const *s2)
{
	if (s1 && s2)
	{
		while (*s1 && *s2 && *s1++ == *s2)
			s2++;
		if (*((unsigned char*)s1) - *((unsigned char*)s2) == 0)
			return (1);
	}
	return (0);
}
