int		ft_atoi(const char *str)
{
	long	n;
	int		flag;

	n = 0;
	flag = 1;
	while (*str >= '\0' && ((*str >= 9 && *str <= 13) || *str == ' '))
		str++;
	if (*str == '-' && (flag = -1))
		str++;
	if (*str == '+' && flag == 1)
		str++;
	while (*str >= '0' && *str <= '9')
	{
		if (n > (n * 10 + *str - '0'))
		{
			if (flag == -1)
				return (0);
			else
				return (-1);
		}
		n = n * 10 + *str - '0';
		str++;
	}
	return ((int)(n * flag));
}
