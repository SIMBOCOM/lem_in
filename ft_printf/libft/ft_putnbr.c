#include "libft.h"

void	ft_putnbr(int n)
{
	long n1;

	n1 = n;
	if (n1 < 0)
	{
		ft_putchar('-');
		n1 *= -1;
	}
	if (n1 < 10)
		ft_putchar(n1 + '0');
	else
	{
		ft_putnbr(n1 / 10);
		ft_putchar(n1 % 10 + '0');
	}
}
